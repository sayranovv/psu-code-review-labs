"""
HomeDyn17 — К-ичные числа
========================
Подсчитать количество N-разрядных чисел в системе счисления с основанием K,
в записи которых НЕ содержится более двух подряд идущих нулей.

Подход: динамическое программирование.

Состояние dp[i][z] — количество i-разрядных «хвостов» (суффиксов),
где z — количество нулей на хвосте (0, 1 или 2):
  z=0: последняя цифра ненулевая
  z=1: последняя цифра — ровно один ноль
  z=2: последние две цифры — ровно два нуля

Переходы для очередной цифры d:
  d ≠ 0  →  текущий z становится 0 (серия нулей прервана)
  d = 0  →  текущий z становится min(prev_z + 1, 3); если z≥3 — запрещено

Первый разряд (старший) не может быть 0 (N-разрядное число).
"""

import sys


def solve(n: int, k: int) -> int:
    """
    Возвращает количество N-разрядных K-ичных чисел
    без трёх и более подряд идущих нулей.
    """
    # dp[z] = количество чисел текущей длины, у которых хвост из z нулей
    # z ∈ {0, 1, 2}

    # Инициализация: первая цифра — от 1 до K-1 (ведущий ноль запрещён)
    dp = [0, 0, 0]
    dp[0] = k - 1  # (k-1) ненулевых цифр → хвост нулей = 0

    # Наращиваем разряды 2..N
    for _ in range(n - 1):
        new_dp = [0, 0, 0]
        for z in range(3):
            if dp[z] == 0:
                continue
            # Добавляем ненулевую цифру (k-1 вариантов)
            new_dp[0] += dp[z] * (k - 1)
            # Добавляем ноль — новый хвост = z+1
            if z + 1 <= 2:
                new_dp[z + 1] += dp[z]
            # z+1 == 3 → три нуля подряд → запрещено, не учитываем

        dp = new_dp

    return sum(dp)


def solve_bruteforce(n: int, k: int) -> int:
    """
    Перебор всех N-разрядных K-ичных чисел — для верификации (только при n≤7).
    """
    count = 0
    start = k ** (n - 1)  # первое N-разрядное число
    end   = k ** n         # первое (N+1)-разрядное число

    for num in range(start, end):
        # Перевод в K-ичную запись (ровно n разрядов)
        digits = []
        tmp = num
        while tmp:
            digits.append(tmp % k)
            tmp //= k
        # Проверка: нет трёх подряд нулей
        zeros = 0
        ok = True
        for d in reversed(digits):
            if d == 0:
                zeros += 1
                if zeros > 2:
                    ok = False
                    break
            else:
                zeros = 0
        if ok:
            count += 1
    return count


def main() -> None:
    # Ввод параметров
    if len(sys.argv) == 3:
        n = int(sys.argv[1])
        k = int(sys.argv[2])
    else:
        print("HomeDyn17 — К-ичные числа без трёх подряд нулей")
        print("Введите N и K (через пробел): ", end="")
        n, k = map(int, input().split())

    # Валидация
    if not (1 < n < 20):
        print("Ошибка: N должно быть в диапазоне 2..19")
        sys.exit(1)
    if not (2 <= k <= 10):
        print("Ошибка: K должно быть в диапазоне 2..10")
        sys.exit(1)
    if n + k >= 26:
        print("Ошибка: условие N+K<26 не выполнено")
        sys.exit(1)

    result = solve(n, k)

    print(f"\nСистема счисления: {k}-ичная")
    print(f"Количество разрядов: {n}")
    print(f"\nОтвет: {result}")

    # Проверка брутфорсом для небольших n
    if n <= 7:
        bf = solve_bruteforce(n, k)
        match = "✓ совпадает" if bf == result else f"✗ расхождение! брутфорс={bf}"
        print(f"Проверка (брутфорс): {bf}  {match}")


if __name__ == "__main__":
    main()
