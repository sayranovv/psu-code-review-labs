def solve():
    try:
        n = int(input("Введите N (количество разрядов): "))
        k = int(input("Введите K (основание системы): "))
    except ValueError:
        print("Пожалуйста, введите целые числа.")
        return

    if not (1 < n < 20 and 2 <= k <= 10):
        print("Введенные данные не соответствуют ограничениям задачи.")
        return

    # dp[i][z], где i - длина, z - кол-во нулей в конце (0, 1, 2)
    dp = [[0] * 3 for _ in range(n + 1)]

    # Базовый случай: числа длины 1
    # Первая цифра не может быть нулем, поэтому:
    dp[1][0] = k - 1  # Цифры от 1 до K-1
    dp[1][1] = 0      # Число из одного разряда не может начинаться с 0
    dp[1][2] = 0

    for i in range(2, n + 1):
        # Заканчивается не на ноль: приписываем любую из (K-1) цифр к любому валидному числу
        dp[i][0] = (dp[i-1][0] + dp[i-1][1] + dp[i-1][2]) * (k - 1)
        
        # Заканчивается на один ноль: приписываем 0 к тому, что заканчивалось не на 0
        dp[i][1] = dp[i-1][0]
        
        # Заканчивается на два нуля: приписываем 0 к тому, что заканчивалось на один 0
        dp[i][2] = dp[i-1][1]

    # Общее количество - сумма всех вариантов для длины N
    total = sum(dp[n])
    
    print(f"Количество {n}-разрядных {k}-ичных чисел: {total}")

if __name__ == "__main__":
    solve()
